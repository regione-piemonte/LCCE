package it.csi.solconfig.configuratoreweb.presentation.model;

public class FunzionalitaTreeDTO {

    private Long id;

    private Long parent;

    public FunzionalitaTreeDTO(Long id, Long parent) {
        this.id = id;
        this.parent = parent;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getParent() {
        return parent;
    }

    public void setParent(Long parent) {
        this.parent = parent;
    }
}
